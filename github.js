function getAllFiles(ghrepo, dir, files) {
    
    ghrepo.contents(dir, function(err, data) {
        var str = data;
        if (str == null) return;
        for (var i = 0; i < str.length; i++) {
            var obj = str[i];
            var path = obj.path;
            var type = obj.type;
            if (type == 'dir') {
                getAllFiles(ghrepo, dir + path + '/', files);
            } else {
                files[files.length] = path;
            }
            console.log('Path: ' + obj.path + ' Type: ' + obj.type);
        }
    });
}

